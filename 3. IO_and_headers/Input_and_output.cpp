#include<iostream>
using namespace std;

int main()
{
    int num1, num2;
    cout<<"Enter the value of First number:\n";/*This is called Insertion operator*/
    cin>>num1;/*This is called Extraction operator*/

    cout<<"Enter the value of second number:\n";
    cin>>num2;

    cout<<"The sum of the 2 numbers is: "<<num1+num2;
    return 0;
}
